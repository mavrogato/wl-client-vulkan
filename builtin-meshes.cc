    template <size_t N>
    consteval auto build_vertices(std::span<aux::vec3f const, N> positions,
                                  std::span<aux::vec3f const, N> normals) noexcept {
        std::array<builtin::mesh::vertex, N> result;
        for (size_t idx = 0; auto position : rectangular_normalize(positions)) {
            result[idx] = {
                .position = position,
                .normal = normals[idx],
            };
            ++idx;
        }
        return result;
    }

